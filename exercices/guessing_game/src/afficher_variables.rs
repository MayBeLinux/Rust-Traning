fn main() {
    let x = 10;
    let y = 20;

    println!("x = {x} and y + 2 = {}", y + 2); // Definition D'Interpolation de chaîne 
                                               // L'Interpolation de chaîne est une fonctionnalité qui permet d'insérer des variables ou des expressions directement dans une chaîne de caractères.
                                               // Les accolades {} sont utilisées comme des espaces réservés pour les valeurs qui seront insérées
                                               // Exemple : let name = "Alice"; println!("Hello, {name}!");
                                               // Dans cet exemple, {name} sera remplacé par la valeur de la variable name.
                                               // On peut aussi insérer des expressions plus complexes à l'intérieur des accolades, comme y + 2 dans cet exemple.
                                               // Notez que pour les expressions plus complexes, on utilise des accolades supplémentaires pour délimiter l'expression, comme dans y + 2.
                                               //
}